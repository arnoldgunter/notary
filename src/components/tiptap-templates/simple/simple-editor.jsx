export function SimpleEditor({ note, onUpdate }) {
  const saveTimeout = useRef(null);
  const { width = 1024 } = useWindowSize();
  const isMobile = width <= 480;

  let parsedContent = {
    type: "doc",
    content: [],
  };

  try {
    if (note?.content) {
      const data = JSON.parse(note.content);
      parsedContent = data?.content?.type
        ? data.content
        : data?.type
          ? data
          : parsedContent;
    }
  } catch {
    console.warn("Invalid JSON in note.content");
  }

  const editor = useEditor({
    immediatelyRender: false,
    editorProps: {
      attributes: {
        class: "simple-editor",
      },
    },
    extensions: [
      StarterKit.configure({
        horizontalRule: false,
        link: {
          openOnClick: false,
        },
      }),
      HorizontalRule,
      TextAlign.configure({ types: ["heading", "paragraph"] }),
      TaskList,
      TaskItem.configure({ nested: true }),
      Highlight.configure({ multicolor: true }),
      Image,
      Typography,
      Superscript,
      Subscript,
      Selection,
      ImageUploadNode.configure({
        accept: "image/*",
        maxSize: MAX_FILE_SIZE,
        limit: 3,
        upload: handleImageUpload,
      }),
    ],
    content: parsedContent,

    onUpdate: ({ editor }) => {
      const json = editor.getJSON();
      if (saveTimeout.current) clearTimeout(saveTimeout.current);

      saveTimeout.current = setTimeout(() => {
        if (!note) return;

        let existing = {};
        try {
          existing = JSON.parse(note.content || "{}");
        } catch {}

        const updated = { ...existing, content: json };
        onUpdate(JSON.stringify(updated));
      }, 1000);
    },
  });

  useEffect(() => {
    return () => {
      if (saveTimeout.current) clearTimeout(saveTimeout.current);
    };
  }, []);

  if (!editor) return null;

  return (
    <div className="simple-editor-wrapper">
      <EditorContext.Provider value={{ editor }}>
        <Toolbar>
          <MainToolbarContent
            isMobile={isMobile}
            onHighlighterClick={() => {}}
            onLinkClick={() => {}}
          />
        </Toolbar>
        <EditorContent editor={editor} className="simple-editor-content" />
      </EditorContext.Provider>
    </div>
  );
}
